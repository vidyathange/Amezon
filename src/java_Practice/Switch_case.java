package java_Practice;

public class Switch_case {

	public static void main(String[] args) {
		char grade ='B';
		
		switch (grade){
		 
		case'A':
			System.out.println("Excellent");
		    break;
		    
		case 'B':
			
		    System.out.println("Good");
			break;
		case'C':
			System.out.println("Avarage");
		    break;
		case'D':    
			System.out.println("Invalid grade"); 
		    break;
		}

	}

}
